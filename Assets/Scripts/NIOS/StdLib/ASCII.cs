
/// <summary>
/// Information from http://www.asciitable.com/
/// Characters that do not have textual representation.
/// </summary>
public static class ASCII
{
	/// <summary>
	/// null
	/// </summary>
	public const char NUL = (char)0x0;
	/// <summary>
	/// Start Of Heading
	/// </summary>
	public const char SOH = (char)0x1;
	/// <summary>
	/// Start Of Text
	/// </summary>
	public const char STX = (char)0x2;
	/// <summary>
	/// End Of Text
	/// </summary>
	public const char ETX = (char)0x3;
	/// <summary>
	/// End Of Transmission
	/// </summary>
	public const char EOT = (char)0x4;
	/// <summary>
	/// ENQuiry
	/// </summary>
	public const char ENQ = (char)0x5;
	/// <summary>
	/// ACKnowledge
	/// </summary>
	public const char ACK = (char)0x6;
	/// <summary>
	/// BELl
	/// </summary>
	public const char BEL = (char)0x7;
	/// <summary>
	/// Back Space, \b
	/// </summary>
	public const char BS = (char)0x8;
	/// <summary>
	/// horizontal TAB
	/// </summary>
	public const char TAB = (char)0x9;
	/// <summary>
	/// new line, \n, Line Feed
	/// </summary>
	public const char LF = (char)0xA;
	/// <summary>
	/// Vertical Tab
	/// </summary>
	public const char VT = (char)0xB;
	/// <summary>
	/// new page, Form Feed
	/// </summary>
	public const char FF = (char)0xC;
	/// <summary>
	/// Carrigae Return, \r
	/// </summary>
	public const char CR = (char)0xD;
	/// <summary>
	/// Shift Out
	/// </summary>
	public const char SO = (char)0xE;
	/// <summary>
	/// Shift In
	/// </summary>
	public const char SI = (char)0xF;
	/// <summary>
	/// Data Link Escape
	/// </summary>
	public const char DLE = (char)0x10;
	/// <summary>
	/// Device Control 1
	/// </summary>
	public const char DC1 = (char)0x11;
	/// <summary>
	/// Device Control 2
	/// </summary>
	public const char DC2 = (char)0x12;
	/// <summary>
	/// Device Control 3
	/// </summary>
	public const char DC3 = (char)0x13;
	/// <summary>
	/// Device Control 4
	/// </summary>
	public const char DC4 = (char)0x14;
	/// <summary>
	/// Negative AcKnowledge
	/// </summary>
	public const char NAK = (char)0x15;
	/// <summary>
	/// SYNchronous idle
	/// </summary>
	public const char SYN = (char)0x16;
	/// <summary>
	/// End of Trans. Block
	/// </summary>
	public const char ETB = (char)0x17;
	/// <summary>
	/// CANcel
	/// </summary>
	public const char CAN = (char)0x18;
	/// <summary>
	/// End Of Medium
	/// </summary>
	public const char EM = (char)0x19;
	/// <summary>
	/// SUBstitue
	/// </summary>
	public const char SUB = (char)0x1A;
	/// <summary>
	/// ESCape
	/// </summary>
	public const char ESC = (char)0x1B;
	/// <summary>
	/// File Separator
	/// </summary>
	public const char FS = (char)0x1C;
	/// <summary>
	/// Group Separator
	/// </summary>
	public const char GS = (char)0x1D;
	/// <summary>
	/// Record Separator
	/// </summary>
	public const char RS = (char)0x1E;
	/// <summary>
	/// Unit Separator
	/// </summary>
	public const char US = (char)0x1F;

	/// <summary>
	/// DELete
	/// </summary>
	public const char DEL = (char)0x7F;


	public const char BackSpace = BS;
	public const char NewLine = LF;
	public const char Escape = ESC;
	public const char CarriageReturn = CR;
	public const char Space = (char)0x20;
}
